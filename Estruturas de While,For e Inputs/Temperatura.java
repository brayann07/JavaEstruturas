public class Temperatura{
    public static void main(String[] args){
        System.out.println("Exemplo 1");
        double C,F,K;
        C = 20;
        // calcular formulas
        F = C * 1.8 + 32;
        K = C * 273.15;
        System.out.println("Temperatura em Fahrenheit: " + F);
        System.out.println("Temperatura em Kelvin: " + K);
    }
}





 